#include "bsq.h"

void	free_map(t_map *map)
{
	int	r;

	r = 0;
	while (r < map->row)
	{
		free(map->map[r]);
		map->map[r] = 0;
		r++;
	}
	free(map->map);
	map->map = 0;
}
