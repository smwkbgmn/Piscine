int	plus(int n1, int n2)
{
	return (n1 + n2);
}

int	minus(int n1, int n2)
{
	return (n1 - n2);
}

int	div(int n1, int n2)
{
	return (n1 / n2);
}

int	multiple(int n1, int n2)
{
	return (n1 * n2);
}

int	mod(int n1, int n2)
{
	return (n1 % n2);
}
