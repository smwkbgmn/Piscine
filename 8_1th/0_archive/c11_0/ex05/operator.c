long long	plus(int n1, int n2)
{
	return (n1 + n2);
}

long long	minus(int n1, int n2)
{
	return (n1 - n2);
}

long long	div(int n1, int n2)
{
	return (n1 / n2);
}

long long	multiple(int n1, int n2)
{
	return (n1 * n2);
}

long long	mod(int n1, int n2)
{
	return (n1 % n2);
}
